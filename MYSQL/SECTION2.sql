
SHOW DATABASES;

USE S1;

SELECT * FROM CLIENT;

-- OU
SELECT NAME, GENDER, ADDRESS
FROM CLIENT
WHERE GENDER="F" OR ADDRESS LIKE "%ESTACIO";

-- E
SELECT NAME, GENDER, ADDRESS
FROM CLIENT
WHERE GENDER="M" AND ADDRESS LIKE "%RJ";


-- COUNT(*)
SELECT COUNT(*) AS TOTAL
FROM CLIENT;

-- GROUP BY
SELECT GENDER, COUNT(*)
FROM CLIENT
GROUP BY GENDER;


